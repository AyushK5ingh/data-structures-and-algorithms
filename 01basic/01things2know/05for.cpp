#include <bits/stdc++.h>
using namespace std;
int main() {
    int i=0;
    for(int i=0;i++;i<5)
    {
        cout<<i<<endl;
    }
    while(i<15)
    {
        cout<<i<<endl;
        i++;
    }
    //do while loop
    //execute the loop once and then check the condition
    do
    {
        cout<<i<<endl;
        i++;
    } while(i<5);
}
